// ─── Helper: transliterate a string using a character map ───
function transSimple(str, map) {
  let out = '';
  for (const ch of str) {
    out += map[ch] ?? ch;
  }
  return out;
}

// ─── Helper: transliterate with digraphs (Greek) ───
function transDigraph(str, { map, digraphs }) {
  let out = '';
  let i = 0;
  while (i < str.length) {
    let matched = false;
    for (const [d, r] of digraphs) {
      if (str.slice(i, i + d.length) === d) {
        out += r;
        i += d.length;
        matched = true;
        break;
      }
    }
    if (matched) continue;
    out += map[str[i]] ?? str[i];
    i++;
  }
  return out;
}

// ─── Helper: transliterate using jamo-based approach (Korean) ───
function transKorean(str) {
  const { init, vowel, fin } = MAPS.ko;
  const ga = 0xAC00;
  const out = [];
  for (const ch of str) {
    const cp = ch.codePointAt(0);
    if (cp >= ga && cp < 0xD7A4) {
      const s = cp - ga;
      const i = Math.floor(s / 588);
      const v = Math.floor((s % 588) / 28);
      const f = s % 28;
      const iStr = init[String.fromCharCode(0x1100 + i)] || '';
      const vStr = vowel[String.fromCharCode(0x1161 + v)] || '';
      const fStr = f ? fin[String.fromCharCode(0x11A7 + f)] || '' : '';
      out.push(iStr + vStr + fStr);
    } else {
      out.push(ch);
    }
  }
  return out.join('');
}

// ─── Helper: ISO 15919 for Devanagari and similar Indic ───
function transIndic(str, { vowel, cons, dep }) {
  const out = [];
  const chars = [...str];
  let i = 0;
  while (i < chars.length) {
    const ch = chars[i];
    if (dep[ch]) {
      out.push(dep[ch]);
      i++;
      continue;
    }
    if (vowel[ch]) {
      out.push(vowel[ch]);
      i++;
      continue;
    }
    if (cons[ch]) {
      const next = chars[i + 1];
      if (next && next === '्') {
        out.push(cons[ch]);
        out.push('a');
        i += 2;
      } else if (next && dep[next] && next !== '्') {
        out.push(cons[ch] + dep[next]);
        i += 2;
      } else {
        out.push(cons[ch] + 'a');
        i++;
      }
      continue;
    }
    if (/[\u0966-\u096F]/.test(ch)) {
      out.push('0123456789'[ch.codePointAt(0) - 0x0966]);
      i++;
      continue;
    }
    out.push(ch);
    i++;
  }
  return out.join('').replace(/[ṅñṇnm]a(?=[āiīuūeoaio])/g, (m) => m.slice(0, -1));
}

// ─── Main transliteration function ───
export function transliterate(text, langCode) {
  if (!text) return '';
  const m = MAPS[langCode];
  if (!m) return null;

  if (langCode === 'el') return transDigraph(text, m);
  if (langCode === 'ko') return transKorean(text);
  if (['hi','mr','ne','bh','bn','as','pa','gu','ta','te','kn','ml','si'].includes(langCode)) return transIndic(text, m);
  return transSimple(text, m);
}

export function hasTranslit(langCode) {
  return langCode in MAPS;
}
